# Tipos Básicos
print(type(1)) # Inteiros
print(type(1.1)) # Float
print(type('Texto')) # String 
print(type(False)) # Booleano
print(type(True)) # Booleano
print(type([1, 2, 3])) # Lista
print(type({'nome': 'Danniel', 'idade': 26 })) # Dicionario
print(type(None)) # Nulo

print('Você é ' + 3 * 'Muito ' + 'Legal!') # Concatenação
print(1.2 + 1) # Soma real
print(3 + '3') # Ambiguidade
