'''
Uma pessoa percorre uma distancia de 15 quilometros a uma velocidade média de 3 horas. 
1 - Calcule o tempo estimado em horas.

Tempo Estimado = Distãncia / Velocidade média
''' 

distancia = 15
velocidade_media = 3

tempo_estimado = distancia / velocidade_media

print(f'A pessoa percorreu {distancia}km a uma velocidade média de {tempo_estimado:.0f}km/h')
